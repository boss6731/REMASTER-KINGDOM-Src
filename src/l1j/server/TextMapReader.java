/**
 * 讀取文本地圖(maps/\d*.txt)。
 */
public class TextMapReader extends MapReader {

	/** 地圖目錄。 */
	private static final String MAP_DIR = "./maps/";
	/** MAP_INFO中地圖編號的位置。 */
	public static final int MAPINFO_MAP_NO = 0;
	/** MAP_INFO中開始X坐標的位置。 */
	public static final int MAPINFO_START_X = 1;
	/** MAP_INFO中結束X坐標的位置。 */
	public static final int MAPINFO_END_X = 2;
	/** MAP_INFO中開始Y坐標的位置。 */
	public static final int MAPINFO_START_Y = 3;
	/** MAP_INFO中結束Y坐標的位置。 */
	public static final int MAPINFO_END_Y = 4;

	/**
	 * 讀取指定地圖編號的文本地圖。
	 * 
	 * @param mapId
	 *            地圖編號
	 * @param xSize
	 *            X坐標的大小
	 * @param ySize
	 *            Y坐標的大小
	 * @return byte[][]
	 * @throws IOException
	 */
	public byte[][] read(final int mapId, final int xSize, final int ySize) throws IOException {
		byte[][] map = new byte[xSize][ySize];
		LineNumberReader in = new LineNumberReader(new BufferedReader(new FileReader(MAP_DIR + mapId + ".txt")));

		int x, y = 0;
		String line;
		byte tile;
		StringTokenizer tok = null;
		while ((line = in.readLine()) != null) {
			if (line.trim().length() == 0 || line.startsWith("#")) {
				continue; // 空行或注釋跳過
			}

			x = 0;
			tok = new StringTokenizer(line, ",");
			while (tok.hasMoreTokens()) {
				tile = (byte) (Integer.parseInt(tok.nextToken()) & 0xff);
				map[x][y] = tile;
				x++;
			}
			y++;
		}
		in.close();
		return map;
	}

	/**
	 * 讀取指定地圖編號的文本地圖。
	 * 
	 * @param id
	 *            地圖編號
	 * @return L1Map
	 * @throws IOException
	 */
	@Override
	public L1Map read(final int id) throws IOException {
		L1V1Map map = null;
		for (int[] info : MAP_INFO) {
			int mapId = info[MAPINFO_MAP_NO];
			int xSize = info[MAPINFO_END_X] - info[MAPINFO_START_X] + 1;
			int ySize = info[MAPINFO_END_Y] - info[MAPINFO_START_Y] + 1;

			if (mapId == id) {
				map = new L1V1Map((short) mapId, this.read(mapId, xSize, ySize), info[MAPINFO_START_X], info[MAPINFO_START_Y], MapsTable.getInstance().isUnderwater(mapId),
						MapsTable.getInstance().isMarkable(mapId), MapsTable.getInstance().isTeleportable(mapId), MapsTable.getInstance().isEscapable(mapId),
						MapsTable.getInstance().isUseResurrection(mapId), MapsTable.getInstance().isUsePainwand(mapId), MapsTable.getInstance().isEnabledDeathPenalty(mapId),
						MapsTable.getInstance().isTakePets(mapId), MapsTable.getInstance().isRecallPets(mapId), MapsTable.getInstance().isUsableItem(mapId),
						MapsTable.getInstance().isUsableSkill(mapId), MapsTable.getInstance().isRuler(mapId), MapsTable.getInstance().isPCTEL(mapId),
						false);
				return map;
			}
		}
		throw new FileNotFoundException("地圖編號: " + id);
	}

	/**
	 * 讀取所有文本地圖。
	 * 
	 * @return Map
	 * @throws IOException
	 */
	@Override
	public Map<Integer, L1Map> read() throws IOException {
		Map<Integer, L1Map> maps = new HashMap<Integer, L1Map>();
		L1V1Map map = null;
		for (int[] info : MAP_INFO) {
			int mapId = info[MAPINFO_MAP_NO];
			int xSize = info[MAPINFO_END_X] - info[MAPINFO_START_X] + 1;
			int ySize = info[MAPINFO_END_Y] - info[MAPINFO_START_Y] + 1;

			try {
				map = new L1V1Map((short) mapId, this.read(mapId, xSize, ySize), info[MAPINFO_START_X], info[MAPINFO_START_Y], MapsTable.getInstance().isUnderwater(mapId),
						MapsTable.getInstance().isMarkable(mapId), MapsTable.getInstance().isTeleportable(mapId), MapsTable.getInstance().isEscapable(mapId),
						MapsTable.getInstance().isUseResurrection(mapId), MapsTable.getInstance().isUsePainwand(mapId), MapsTable.getInstance().isEnabledDeathPenalty(mapId),
						MapsTable.getInstance().isTakePets(mapId), MapsTable.getInstance().isRecallPets(mapId), MapsTable.getInstance().isUsableItem(mapId),
						MapsTable.getInstance().isUsableSkill(mapId), MapsTable.getInstance().isRuler(mapId), MapsTable.getInstance().isPCTEL(mapId),
						false);

				maps.put(mapId, map);
			} catch (IOException e) {
				e.printStackTrace();
			}
		}

		return maps;
	}
}

