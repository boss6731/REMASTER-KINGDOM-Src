 package l1j.server.server.model;

 import l1j.server.server.model.Instance.L1ItemInstance;
 import l1j.server.server.model.Instance.L1PcInstance;
 import l1j.server.server.serverpackets.S_ItemStatus;
 import l1j.server.server.serverpackets.ServerBasePacket;






























































































































































































































































































































































































































































 class ArmorSetItem
   implements L1ArmorSetEffect
 {
   private int main_itemid;

   public ArmorSetItem(int itemid) {
     this.main_itemid = itemid;
   }


   public void giveEffect(L1PcInstance pc) {
     L1ItemInstance set_item = pc.getInventory().findEquippedItemId(this.main_itemid);
     if (set_item != null) {
       set_item.set_main_set_armor(true);
       pc.sendPackets((ServerBasePacket)new S_ItemStatus(set_item));
     }
   }


   public void cancelEffect(L1PcInstance pc) {
     L1ItemInstance[] items = pc.getInventory().findItemsId(this.main_itemid);
     if (items != null)
       for (L1ItemInstance item : items) {
         item.set_main_set_armor(false);
         pc.sendPackets((ServerBasePacket)new S_ItemStatus(item));
       }
   }
 }


